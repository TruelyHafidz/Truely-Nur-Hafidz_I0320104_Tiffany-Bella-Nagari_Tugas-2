print("Konversi suhu (celcius ke farenheit dan reamur ke kelvin)")

print("1. Menghitung konversi suhu (celcius ke farenheit")

#input suhu dalam celcius
C = float(input("Masukkan suhu (celcius): "))

#menghitung konversi suhu ke farenheit
F = 9 * C / 5 + 32

#menampilkan hasil perhitungan
print("farenheit; ", F)

print("2. Menghitung konversi suhu (reamur ke kelvin")

#input suhu dalam reamur
R = float(input("Masukkan suhu (reamur): "))

#menghitung konversi suhu ke kelvin
K = 5 * R / 4 + 273

#menampilkan hasil perhitungan
print("kelvin: ", K)








